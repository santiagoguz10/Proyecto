            <p>NOTICIAS</p>
            <p>“Esta mañana, vamos a aprender a programar. ¿Alguien sabe lo que significa?”, pregunta Carlos Fernández, voluntario de la Hora del Código. Son las 10.00 y los niños del CE Ponce de León en Madrid están un poco dormidos todavía. Irene, de 10 años, se lanza a preguntar: “¿es una serie?”. “Sí. Programar es una serie de instrucciones que le das a la máquina para que haga una determinada tarea. Desde una aplicación del móvil hasta los ordenadores”, completa Fernández.</p>


<p>En 2012 los hermanos Ali y Hadi Partovi, de origen iraní, fundaron Code.org en Silicon Valley, una organización sin ánimo de lucro que promueve la participación en escuelas e institutos de las Ciencias de la Computación. Y desde hace un par de años, lanzaron la Hora del Código, un movimiento global que llega a decenas de millones de estudiantes en más de 180 países. Cualquier persona puede organizar una hora de programación y en cualquier lugar. El objetivo es que todo el mundo pueda aprender las Ciencias de la Computación y comprender sus fundamentos básicos, a través de tutoriales colgados en la web Code.org. Cada año se celebra a principios de diciembre con ocasión de la Semana Educativa de las Ciencias de la Computación.

<p>MÁS INFORMACIÓN</p>
<p>Un robot en clase para que los niños aprendan a programar</p>
La escuela del futuro ya existe en Singapur
Enseñar programación para combatir el paro juvenil en España
Este colegio concertado del sur de la capital cuenta con alumnos discapacitados, en su mayoría niños con deficiencia auditiva, pero no les impide aprender a programar como los demás. “La mayor dificultad es tomar el tiempo de leer las consignas para saber qué tienen que hacer para hacer avanzar al muñeco”, comenta Paco Francisco, otro voluntario de Accenture. Es una empresa que se dedica a la tecnología y asesora a otras organizaciones.</p>

<p>A lo largo de la sesión, los alumnos de 5º se animan y empiezan a competir entre ellos para pasar los niveles de los juegos antes que los demás. En la página de Code.org, tienen a disposición una gran propuesta de actividades. “Los niños reaccionan muy bien, creen que están jugando, pero aprenden. Ellos no se dan cuenta todavía, pero saber cómo funciona la tecnología es primordial para poder usarla de forma más eficiente y más sencilla”, explica Fernández. Hugo tiene 11 años y está programando con un juego de Minecraft. Explica con facilidad cómo ha alcanzado el nivel 13, no le ha costado nada pillar la lógica de la actividad.</p>

<p>-¿Te gusta la programación?</p>

<p>- Sí, es muy interesante y divertido.</p>

<p>- ¿Te gustaría ser programador?</p>

<p>- No sé todavía cuánto se cobra, pero por qué no.</p>

<p>“El lenguaje computacional debería ser como cualquier otro lenguaje que se aprende”, explica Isabel Navarro, cofundadora de la fundación Créate. Es una organización que trabaja con docentes y centros educativos para un nuevo aprendizaje en el aula. Llevan un par de años trabajando con la robótica y desde el año pasado movilizan a empresarios para promover el aprendizaje de la programación, para la Hora del Código. “Les encanta a los niños. Es una forma muy motivadora de aprender a programar, muy intuitiva. Tengo tres hijos y les encanta”, explica Navarro.</p>

<p>Una coalición de socios, entre los que se incluyen Apple, Microsoft o Amazon, se ha reunido para apoyar la Hora del Código, Boys y Girls Clubs of America y College Board, así como varios famosos que promueven el encuentro, de Barack Obama a Ashton Kutcher pasando por Sergio Ramos. “Estamos rodeados de tecnología, nos está afectando en cuanto a nuestras relaciones humanas, cómo nos comportamos, comunicamos. Entonces comprender cómo funciona esa tecnología es importantísimo”, sostiene Navarro.</p>

<p>Australia, Argentina, Italia, Reino Unido, Corea del Sur, Arabia Saudí y Estados Unidos fueron los primeros países a dar el paso en este sentido. En España, fueron las comunidades autónomas las primeras en actuar. En el caso de Madrid, los alumnos de 1º a 4º tienen una asignatura obligatoria que se llama ‘Tecnología, Programación y Robótica’, y poco a poco los colegios empiezan a dar clases de programación a sus alumnos de primero aunque no tengan la obligación. Como en el CEIP de Ciudad de Nejapa (Madrid) donde han apostado por las nuevas tecnologías. Llevan varios años introduciendo nuevas herramientas – pizarras, tablets – y ahora la programación. “Estos niños de 5º llegarán con un buen bagaje a la educación secundaria. Cuanto antes se empiece mejor. De hecho, hemos preparado una nueva asignatura para el año que viene y esperamos la respuesta de la administración para poder impartirla. Vamos a comprar kits para aprender a programar, pero contrariamente a los colegios privados, no pedimos más dinero a los padres, usamos el presupuesto que nos da la comunidad”, explica con entusiasmo Patricia Ibarra, profesora de matemáticas y coordinadora del área de tecnología del centro. Isabel y Marta tienen 10 años y trabajan en pareja. Ellas tampoco tienen dificultades para hacer avanzar los avatares de la actividad. Eligieron el juego basado en Star Wars.</p>

<p>- La verdad que programar es muy divertido, me encanta la peli de Star Wars. Y no es tan difícil, pero depende de los niveles, matiza Marta.</p>

<p>- ¿Ya habíais programado antes?</p>

<p>- Yo sí, contesta orgullosa Isabel. En casa con mis hermanas.</p>

<p>Gran parte de los niños no son extraños a la programación o a la robótica. Aprenden en familia, los padres les compran robots que pueden construir ellos mismos y programarles para que hablen y caminen. Está lejos el tiempo de las muñecas Barbie y de los Lego tradicionales. Ahora con Lego también se programa. Los niños de infantil usan aplicaciones para crear juegos como Tinytab o Makeit. Y está claro que prefieren trabajar en aplicaciones más que en papel. “Era inconcebible hace unos años y va avanzando cada vez más, por eso es tan importante que los niños aprendan a programar desde temprano”, sostiene Ibarra.</p>
            
            
         

 
